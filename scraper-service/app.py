"""
ArrowLabs scraper service.
Uses curl_cffi (Chrome TLS impersonation) to bypass Amazon's bot-wall from a
datacenter IP, then parses product data, reviews, and competitor search results.
Runs locally on the VM; the Next.js app calls it as the primary Amazon provider.

Endpoints:
  GET /health
  GET /product?asin=ASIN&domain=amazon.com   -> product + embedded top reviews
  GET /reviews?asin=ASIN&domain=amazon.com&pages=2  -> deeper reviews
  GET /search?q=keyword&domain=amazon.com&limit=10  -> competitor listings
"""

