@app.get("/scrape")
def scrape():
    """Unified multi-marketplace scrape: Flipkart, Myntra, Noon, Namshi, Nykaa.
    Amazon keeps its dedicated /product route (ASIN + review session)."""
    platform = request.args.get("platform", "").strip().lower()
    pid = request.args.get("id", "").strip()
    region = request.args.get("region", "").strip() or None
    if not platform or not pid:
        return jsonify({"error": "missing platform or id"}), 400
    if platform == "amazon":
        return jsonify({"error": "use /product for amazon"}), 400
    if platform not in marketplaces.PLATFORMS:
        return jsonify({"error": f"unsupported platform {platform}"}), 400

    ck = f"scrape:{platform}:{region}:{pid}"
    cached = cache_get(ck, 600)
    if cached is not None:
        return jsonify(cached)

    url = marketplaces.build_url(platform, pid, region)
    try:
        code, html = fetch(url)
    except Exception as e:
        return jsonify({"error": f"fetch failed: {str(e)[:80]}"}), 502
    if code != 200 or is_botwall(html) or len(html) < 3000:
        return jsonify({"error": f"not found or blocked (HTTP {code})", "url": url}), 404
    data = marketplaces.normalize(platform, html, url)
    if not data.get("title"):
        return jsonify({"error": "no product parsed", "url": url}), 502
    cache_set(ck, data)
    return jsonify(data)


@app.get("/product")
def product():
    asin = request.args.get("asin", "").strip()
    domain = request.args.get("domain", "amazon.com").strip()
    if not asin:
        return jsonify({"error": "missing asin"}), 400
    ck = f"product:{domain}:{asin.upper()}"
    cached = cache_get(ck, 600)
    if cached is not None:
        return jsonify(cached)
    # Try the requested marketplace first, then reachable fallbacks. A direct hit
    # that's blocked returns fast (~2s), so the fallback loop stays well within the
    # app's timeout while still finding the product wherever it actually lives.
    try_domains = [domain] + [d for d in FALLBACK_DOMAINS if d != domain]
    tried = []
    for dom in try_domains:
        url = f"https://www.{dom}/dp/{asin}"
        # Amazon intermittently serves a bot-lite page (200 but no product data) even
        # from residential IPs. A plain retry, then a cookie-warmed session, almost
        # always gets the full page, so we don't give up on the first "noparse".
        data = None
        for attempt in range(4):
            try:
                code, html = (fetch(url) if attempt < 2 else warmed_fetch(url, dom))
            except Exception as e:
                last_err = f"{dom}=err:{str(e)[:30]}"
                time.sleep(1.2)
                continue
            if is_not_found(html):
                last_err = f"{dom}=404"
                break  # genuinely missing on this domain; move to fallback domain
            if code != 200 or is_botwall(html) or len(html) < 5000:
                last_err = f"{dom}={code}/lite"
                time.sleep(1.2)
                continue
            parsed = parse_product(html, asin, dom)
            if not parsed.get("title"):
                last_err = f"{dom}=noparse"
                time.sleep(1.2)
                continue
            data = parsed
            break
        if data:
            data["marketplace"] = dom
            cache_set(f"product:{dom}:{asin.upper()}", data)
            cache_set(ck, data)
            return jsonify(data)
        tried.append(last_err)
    return jsonify({"error": f"not found or blocked (tried {', '.join(tried)})"}), 404


@app.get("/reviews")
def reviews():
    asin = request.args.get("asin", "").strip()
    domain = request.args.get("domain", "amazon.com").strip()
    pages = min(int(request.args.get("pages", "2")), 5)
    if not asin:
        return jsonify({"error": "missing asin"}), 400
    ck = f"reviews:{domain}:{asin.upper()}"
    cached = cache_get(ck, 1800)  # reviews change slowly; 30 min
    if cached is not None:
        return jsonify(cached)
    try:
        # Playwright (rendered) is the reliable path; curl_cffi pages as fallback.
        reviews = render_reviews(asin, domain)
        if not reviews:
            reviews = fetch_reviews_pages(asin, domain, pages)
        result = {"reviews": reviews, "rendered": bool(reviews)}
        if reviews:
            cache_set(ck, result)
        return jsonify(result)
    except Exception as e:
        return jsonify({"error": str(e)}), 502


@app.get("/search")
def search():
    q = request.args.get("q", "").strip()
    domain = request.args.get("domain", "amazon.com").strip()
    limit = min(int(request.args.get("limit", "10")), 20)
    if not q:
        return jsonify({"error": "missing q"}), 400
    ck = f"search:{domain}:{q.lower()}:{limit}"
    cached = cache_get(ck, 600)
    if cached is not None:
        return jsonify(cached)
    try:
        code, html = fetch(f"https://www.{domain}/s?k={quote(q)}")
        if code != 200:
            return jsonify({"error": f"blocked (HTTP {code})"}), 502
        results = parse_search(html, domain, limit)
        if not results:
            results = parse_search(unescape_html(html), domain, limit)
        if not results:
            return jsonify({"error": "no results parsed"}), 502
        out = {"results": results}
        cache_set(ck, out)
        return jsonify(out)
    except Exception as e:
        return jsonify({"error": str(e)}), 502


